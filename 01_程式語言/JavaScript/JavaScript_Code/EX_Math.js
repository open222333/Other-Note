// 數學運算

Math.pow(2, 53) // 2的53次方
Math.round(0.6) // 四捨五入
Math.ceil(0.6) // 無條件進位
Math.floor(0.6) // 無條件捨棄
Math.abs(-5) // 絕對值
Math.max(x, y, z) // 回傳最大值
Math.min(x, y, z) // 回傳最小值
Math.random() // 回傳隨機數 0 - 1.0
Math.PI // 3.14... 圓周率
Math.E // The base of the natural logarithm
Math.sqrt(3) // 平方根
Math.pow(3, 1 / 3) // 3 ** 1/3 立方根
Math.sin(0) // 三角函數 sin
Math.cos(0) // 三角函數
Math.atan(0) // 三角函數
Math.sinh(x)
Math.cosh(x)
Math.tanh(x)
Math.asinh(x)
Math.acosh(x)
Math.atanh(x)
Math.log(10) // 自然對數(natural logarithm)
Math.log(100) / Math.LN10 // 100 的 base 10 對數
Math.log(512) / Math.LN2 // 512 的 base 2 對數
Math.exp(3) // Math.E 立方
Math.cbrt(27) // 立方根
Math.hypot(3, 4) // 所有引數平方和的平方根
Math.log10(100) // 100 的 base 10 對數
Math.log2(1024) // 1024 的 base 2 對數
Math.log1p(x) // (1+x)的自然對數
Math.expm1(x) // Math.exp(x) - 1, Math.log1p() 的反函式
Math.sign(x) // <:-1 ==:0 >:1
Math.imul(2, 3) // 6:32位元整數最佳化過的乘法運算
Math.clz32(0xf) // 28:32位元整數中前導零位元的數目
Math.trunc(3.9) // 去除小數部分轉為整數
Math.fround(x) // 捨入至最接近32位元的浮點數
